    public void fechaIngreso(){
        Calendar ingreso = Calendar.getInstance();
        String dia = Integer.toString(ingreso.get(Calendar.DATE));
        String mes = Integer.toString(ingreso.get(Calendar.MONTH));
        String agno = Integer.toString(ingreso.get(Calendar.YEAR));
        String f_ingreso = agno + "-" + mes + "-" + dia;
        this.jl_timestamp.setText(f_ingreso);
    }
