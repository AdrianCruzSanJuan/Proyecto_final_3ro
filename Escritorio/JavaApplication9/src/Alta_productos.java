    private void jb_insert_altaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_insert_altaMouseClicked
        fechaIngreso();
        String tipo = jcb_tipo.getSelectedItem().toString();
        this.jl_tipo.setText(tipo);
        float precio_com=0.0f;
        precio_com = Float.parseFloat(this.jtf_precio_com.getText());
        float precio_ven=0.0f;
        precio_ven = Float.parseFloat(this.jtf_precio_ven.getText());
        jcb_tipo.getSelectedItem().toString();
        caducidadMetodo();
        String proveedor = this.jtf_id_prov.getText();
        String descripcion=this.jta_descripcion_alta.getText();
        String piguales=this.jtf_piguales_alta.getText();
        int piguales1=Integer.parseInt(piguales);
        if(piguales1 >= 1){
            while(piguales1 >= 1){
                conexion mysql = new conexion();
                Connection con = mysql.getConnection();
                PreparedStatement ps = null;
                String sql = ("INSERT INTO PRODUCTOS(NOMBRE_P, MARCA, CONTENIDO, F_INGRESO, TIPO_PROD, PRECIO_COM, PRECIO_VEN, CADUCIDAD, ID_PROV, DESCRIPCION) VALUES (?,?,?,?,?,?,?,?,?,?);");
                try {
                    ps = (PreparedStatement) con.prepareStatement(sql);
                    ps.setString(1, jtf_nombre_p.getText());
                    ps.setString(2, jtf_marca.getText());
                    ps.setString(3, jtf_contenido.getText());
                    ps.setString(4, jl_timestamp.getText());
                    ps.setString(5, jl_tipo.getText());
                    ps.setString(6, jtf_precio_com.getText());
                    ps.setString(7, jtf_precio_ven.getText());
                    ps.setString(8, jl_caducidad.getText());
                    ps.setString(9, jtf_id_prov.getText());
                    ps.setString(10, jta_descripcion_alta.getText());
                    piguales1 = piguales1 - 1;
                    if (jtf_nombre_p.getText().isEmpty() || jtf_marca.getText().isEmpty() || jtf_contenido.getText().isEmpty() || jl_timestamp.getText().isEmpty() || jl_tipo.getText().isEmpty() || jtf_precio_com.getText().isEmpty() || jtf_precio_ven.getText().isEmpty() || jl_caducidad.getText().isEmpty() || jtf_id_prov.getText().isEmpty() || jta_descripcion_alta.getText().isEmpty()){
                        JOptionPane.showMessageDialog(null, "Los campos no deben estar vacíos");
                    }
                    else{
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Se insertaron los registros exitosamente");}
                }catch (SQLException err){
                    JOptionPane.showMessageDialog(null, "Error, no se pudo ingresar el registro" + err.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jb_insert_altaMouseClicked

